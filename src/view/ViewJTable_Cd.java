    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int sair = JOptionPane.showConfirmDialog(null,"Tem certeza de que deseja excluir?");
    int sairl = Integer.bitCount(sair);
    
    if(sairl == 0){    
          if (jTCd.getSelectedRow() != -1) {

            Cd c = new Cd();
            cdDAO dao = new cdDAO();

            c.setId_cd((int) jTCd.getValueAt(jTCd.getSelectedRow(), 0));
            
            dao.delete(c);

            txtTitulo.setText(null);
            txtPreco.setText(null);
            txtDescricao.setText(null);
            txtQuantidade.setText(null);
           

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um carro para excluir.");
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

