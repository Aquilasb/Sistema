     public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTCd.getModel();
        modelo.setNumRows(0);
        cdDAO cdao = new cdDAO();

        for (Cd c : cdao.read()) {

            modelo.addRow(new Object[]{
                c.getId_cd(),
                c.getNome(),
                c.getPreco(),
                c.getDescricao(),
                c.getQuantidade()
                
               
            });

        }

    }
     
     public void readJTableForTi(String ti) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTCd.getModel();
        modelo.setNumRows(0);
        cdDAO cdao = new cdDAO();

        for (Cd c : cdao.readForTi(ti)) {

            modelo.addRow(new Object[]{
                c.getId_cd(),
                c.getNome(),
                c.getPreco(),
                c.getDescricao(),
                c.getQuantidade()
                
               
            });

        }

    }

