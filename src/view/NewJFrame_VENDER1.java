            Cd t = new Cd();
            venderDAO dao = new venderDAO();
            
       
            t.setId_cd((int) jtVender.getValueAt(jtVender.getSelectedRow(), 0));
            
            
          // c.setNome(jtVender.getValueAt(jtVender.getSelectedRow(), 1).toString()); 
         //  c.setPreco(jtVender.getValueAt(jtVender.getSelectedRow(), 2).toString());  
