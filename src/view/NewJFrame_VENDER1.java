     public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jtVender.getModel();
        modelo.setNumRows(0);
        venderDAO dao = new venderDAO();

        for (Cd tro : dao.read()) {

            modelo.addRow(new Object[]{
                tro.getId_cd(),
                tro.getNome(),
                tro.getPreco(),
                tro.getQuantidade(),
                tro.getVendas()
               
            });

        }

    }

